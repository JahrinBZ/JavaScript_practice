let firstNum = prompt("Enter the 1st number :");
let secondNum = prompt("Enter the 2nd number :");
let operator = prompt("The operator will be : ");

if (operator == '+')
    console.log(firstNum + secondNum);
else if(operator == '-')
    console.log(firstNum - secondNum);
else if (operator == '*')
    console.log(firstNum * secondNum);
else if (operator == '/')
    console.log(firstNum / secondNum);

